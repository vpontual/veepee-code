// ─── Tool registry (P3: hallucinated-tool hard-fail gate) ────────────────────
//
// Loads benchmarks/tool-registry.json (generated by scripts/generate-tool-registry.ts).
// Any tool call whose name is not in (built-in registry ∪ per-test synthetic tools)
// fails its test instantly (score=0). Directly ported from Llama Rider's
// hallucinated_tool_names() safety gate.

let BUILT_IN_TOOL_NAMES: Set<string> = new Set();
try {
  const here = dirname(fileURLToPath(import.meta.url));
  // src/ at dev, dist/ at build-time — registry lives alongside the repo root
  const candidates = [
    resolve(here, '..', 'benchmarks', 'tool-registry.json'),
    resolve(here, '..', '..', 'benchmarks', 'tool-registry.json'),
  ];
  for (const p of candidates) {
    if (existsSync(p)) {
      const parsed = JSON.parse(readFileSync(p, 'utf-8')) as { tools?: string[] };
      BUILT_IN_TOOL_NAMES = new Set(parsed.tools || []);
      break;
    }
  }
} catch {
  // Registry is optional — benchmark still runs without the gate
}

/** Return tool-call names that are neither built-in nor declared in per-test tools. */
function hallucinatedToolNames(
  toolCalls: ToolCallResult[] | undefined,
  testTools: unknown[] | undefined,
): string[] {
  if (!toolCalls || toolCalls.length === 0) return [];
  const allowed = new Set<string>(BUILT_IN_TOOL_NAMES);
  if (testTools) {
    for (const t of testTools) {
      const name = (t as { function?: { name?: string } })?.function?.name;
      if (name) allowed.add(name);
    }
  }
  // If neither source populated anything, skip gate (fail-open, not fail-closed)
  if (allowed.size === 0) return [];
  return toolCalls.map(c => c.name).filter(n => !allowed.has(n));
}

